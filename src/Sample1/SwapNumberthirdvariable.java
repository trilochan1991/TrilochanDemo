package Sample1;

public class SwapNumberthirdvariable {
	
	public static int a = 10;
	public static int b = 20;
	public static int c = 0;
			

	public static void main(String[] args) {
		
		System.out.println(a);
		System.out.println(b);
		c = a;
		a = b;
		b = c;
		System.out.println(a);
		System.out.println(b);

	}

}
